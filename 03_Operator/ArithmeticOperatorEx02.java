// package 03_Operator;

public class ArithmeticOperatorEx02 {
        public static void main(String[] args) {
            int a;
            a = 7 / 2;
            System.out.println(a);
            a = -7 / 2;
            System.out.println(a);
            a = 7 / -2;
            System.out.println(a);
            a = -7 / -2;
            System.out.println(a);

            a = 7 % 2;
            System.out.println(a);
            a = -7 % 2;
            System.out.println(a);
            a = 7 % -2;
            System.out.println(a);
            a = -7 % -2;
            System.out.println(a);
        }

    }